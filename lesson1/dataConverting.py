# Convert string to integer

str_num = '50'
int_num = int(str_num)
print(int_num)  # Output: 50

# Convert string to float

str_num = '50.47'
float_num = float(str_num)
print(float_num)  # Output: 50.47

# Convert string to boolean

str_num = ''
bool_num = bool(str_num)
print(bool_num)  # Output: True

str_num = 'False'

# Convert number to string

int_num = 50
str_num = str(int_num)
print(str_num)  # Output: '50'
